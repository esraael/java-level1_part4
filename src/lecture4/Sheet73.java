package Lecture4;

import java.util.Scanner;

public class Sheet73 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        if (a < b) {
            System.out.println("true");
        }
        if (a >= b) {
            System.out.println("false");
        }
    }

}
